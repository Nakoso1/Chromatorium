/* ドット区切りキー("a.b.c")を辞書から解決する内部ヘルパ */
function _i18nLookup(dict,key){
 return key.split(".").reduce((o,k)=>(o&&Object.prototype.hasOwnProperty.call(o,k))?o[k]:undefined, dict);
}
/* t(key,params?): 現在言語→(未定義なら)日本語→(それも未定義なら)キー名、の順に解決する(§VIII-2)。
   翻訳漏れはコンソール警告で検出可能にする。{name}形式のプレースホルダを置換できる。 */
function t(key,params){
 const cur=(state.ui&&state.ui.lang)||"ja";
 let val=_i18nLookup(I18N[cur]||I18N.ja,key);
 if(val===undefined&&cur!=="ja"){
  console.warn("[i18n] '"+cur+"' に未翻訳キー: "+key+" → jaへフォールバック");
  val=_i18nLookup(I18N.ja,key);
 }
 if(val===undefined){
  console.warn("[i18n] 辞書に存在しないキー: "+key);
  return key;
 }
 if(params){
  Object.keys(params).forEach(k=>{ val=val.replace(new RegExp("\\{"+k+"\\}","g"),params[k]); });
 }
 return val;
}
/* data-i18n / data-i18n-title / data-i18n-ph の付いた全要素へ一括適用する(§VIII-2静的HTML)。
   P0時点ではこれらの属性を持つ要素はまだ存在しない(新規追加分から順次付与する)ため、
   この関数自体は安全に空振りする。 */
